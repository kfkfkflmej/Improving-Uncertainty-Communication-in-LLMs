from html import parser

import pandas as pd
import os
import json
import argparse
from metrics.acc import Accuracy
from metrics.auroc import AUROC
from metrics.ece import ECE

def get_args():
    

    parser = argparse.ArgumentParser(description="Calculate scores for a given dataset and model.")
    parser.add_argument("--data_name", type=str, required=True, help="Name of the dataset (without extension).")
    parser.add_argument("--data_path", type=str, default="results", help="Directory where the results CSV files are stored.")
    parser.add_argument("--auroc_exclude_not_attempted", action="store_true", help="Whether to exclude 'not attempted' samples when calculating AUROC.")
    parser.add_argument("--ece_exclude_not_attempted", action="store_true", help="Whether to exclude 'not attempted' samples when calculating ECE.")
    parser.add_argument("--accuracy_exclude_not_confident", action="store_true", help="Whether to exclude 'not confident' samples when calculating Accuracy.")
    parser.add_argument("--ece_bins", type=int, default=10, help="Number of bins for ECE calculation.")
    parser.add_argument("--output_path", type=str, default="evaluation.jsonl", help="Path to save the evaluation results in JSONL format.")
    args = parser.parse_args()
    return args

def main():
    args = get_args()
    data_name = args.data_name
    results_df = pd.read_csv(os.path.join(args.data_path, f"{data_name}"))


    # Instantiate and evaluate Accuracy
    accuracy_metric = Accuracy(format="simpleqa_like", exclude_not_attempted=args.accuracy_exclude_not_confident)
    accuracy_score = accuracy_metric.evaluate(results_df)

    # Instantiate and evaluate AUROC
    auroc_metric = AUROC(format="simpleqa_like", exclude_not_attempted=args.auroc_exclude_not_attempted)
    auroc_score = auroc_metric.evaluate(results_df)

    # Instantiate and evaluate ECE
    ece_metric = ECE(format="simpleqa_like", exclude_not_attempted=args.ece_exclude_not_attempted, n_bins=args.ece_bins)
    ece_score = ece_metric.evaluate(results_df)
    
    # --- Structure the data row ---
    new_record = {
        "dataset_name": data_name,
        "accuracy_score": accuracy_score,
        "accuracy_exclude_not_confident": args.accuracy_exclude_not_confident,
        "auroc_score": auroc_score,
        "auroc_exclude_not_attempted": args.auroc_exclude_not_attempted,
        "ece_score": ece_score,
        "ece_exclude_not_attempted": args.ece_exclude_not_attempted,
        "ece_bins": args.ece_bins,
    }

    output_path = args.output_path

    with open(output_path, "a", encoding="utf-8") as f:
        f.write(json.dumps(new_record) + "\n")





