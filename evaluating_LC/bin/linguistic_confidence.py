import argparse
import torch
from transformers import AutoTokenizer, AutoModelForCausalLM, pipeline
import pandas as pd
import os
import tqdm
from peft import PeftModel
from transformers import BitsAndBytesConfig
import re
import torch.nn as nn
from transformers import AutoModel


VANILLA_TEMPLATE = """
Answer the following question using a succinct (at most one sentence) and full answer.

Question: {question}
Answer:
""".strip()

VANILLA_UNCERTAINTY_TEMPLATE = """
Answer the following question using a succinct (at most one sentence) and full answer. If you are uncertain about your answer to the question, convey this uncertainty linguistically by precisely hedging this answer.

Question: {question}
Answer:
""".strip()


def get_args():
    parser = argparse.ArgumentParser()
    parser.add_argument('--model_name', type=str, required=True, help="Name of the LLM model (e.g., 'gpt-4o-mini')")
    parser.add_argument('--data_path', type=str, required=True, help="Path to the JSON dataset file")
    parser.add_argument('--dataset', type=str, required=True, help="Name of Dataset")
    parser.add_argument('--temperature', type=float, required=True, help="temperature")
    parser.add_argument('--prompt_style', type=str, required=True, help="Style of the prompt")
    parser.add_argument('--lora_path', type=str, help="Path to the LoRA adapter")
    args = parser.parse_args()
    return args


class LinguisticConfidenceEstimator(nn.Module):
    def __init__(self, model_name: str):
        super().__init__()
        self.model_name = model_name
        self.encoder = AutoModel.from_pretrained(self.model_name)
        self.reg_head = nn.Sequential(
            nn.Linear(self.encoder.config.hidden_size, 1),
            nn.Sigmoid()  # output range in [0, 1]
        )

    def __call__(self, response_df: pd.DataFrame, batch_size: int = 32) -> list[float]:
        tokenizer = AutoTokenizer.from_pretrained(self.model_name)
        device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

        responses = response_df["answer"].fillna("").tolist()
        all_confidences = []

        for i in range(0, len(responses), batch_size):
            batch_responses = responses[i:i + batch_size]

            inputs = tokenizer(
                batch_responses,
                return_tensors="pt",
                truncation=True,
                padding=True,
                max_length=128
            )
            inputs = {k: v.to(device) for k, v in inputs.items()}

            with torch.no_grad():
                output = self.encoder(
                    input_ids=inputs["input_ids"],
                    attention_mask=inputs["attention_mask"]
                )
                cls_hidden = output.last_hidden_state[:, 0]
                confidence_scores = self.reg_head(cls_hidden).squeeze(-1).detach().cpu().numpy()
                all_confidences.extend(confidence_scores.tolist())

        return all_confidences

def get_confidence_mapper(model_name, state_dict_path):
        confidence_estimator = LinguisticConfidenceEstimator(model_name)
        device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
        confidence_estimator.reg_head.load_state_dict(torch.load(state_dict_path))
        return confidence_estimator.to(device)



def main():
    args = get_args()
    print("Running:",args.model_name, " on ", args.data_path, " with ", args.prompt_style)


    # Quantization configuration for 8-bit loading
    quantization_config = BitsAndBytesConfig(
        load_in_8bit=True
    )
    if args.lora_path:
        base_model = AutoModelForCausalLM.from_pretrained(
            args.model_name,
            quantization_config=quantization_config,
            device_map="auto"
        )

        model = PeftModel.from_pretrained(base_model, args.lora_path)
        style = "fine_tuned"
    else:
        model = AutoModelForCausalLM.from_pretrained(
            args.model_name,
            quantization_config=quantization_config,
            device_map="auto"
        )
        style = "lc_plus" if args.prompt_style == "vanilla_uncertainty" else "lc"

    data=pd.read_csv(f"{args.data_path}.csv")
    if  args.prompt_style == "vanilla":
        qa_prompts = [VANILLA_TEMPLATE.format(question=row["question"]) for _, row in data.iterrows()]

    elif args.prompt_style == "vanilla_uncertainty":
        qa_prompts = [VANILLA_UNCERTAINTY_TEMPLATE.format(question=row["question"]) for _, row in data.iterrows()]

    # Set pad_token_id if it's not defined (common for text generation models)
    tokenizer = AutoTokenizer.from_pretrained(args.model_name)
    if tokenizer.pad_token_id is None:
        tokenizer.pad_token_id = tokenizer.eos_token_id

    pipe = pipeline(
        "text-generation", # Changed task to text-generation
        model=model,
        tokenizer=tokenizer,
        dtype=torch.bfloat16 # Use torch_dtype for the pipeline
    )
    responses_raw = []
    batch_size = 8 # Adjust batch_size based on your GPU memory and model size

    for i in tqdm.tqdm(range(0, len(qa_prompts), batch_size), desc="Generating responses"):
        batch_prompts = qa_prompts[i:i + batch_size]
        batch_responses = pipe(batch_prompts, return_full_text=False)
        responses_raw.extend(batch_responses)

    responses = [item[0]['generated_text'].strip() for item in responses_raw]

    response_df = pd.DataFrame({'question': data["question"], 'answer': responses})
    
    base_mapper_name="distilroberta-base"
    log_head_path = "pretrained_weights/human_anno_trained_reg_head.pth"
    confidence_mapper=get_confidence_mapper(base_mapper_name, log_head_path)
    
    confidences = confidence_mapper(data)
    data["confidences"] = confidences

    safe_model_size = re.search(r"(\d+)b", args.model_name)
    if args.dataset == "simple_qa_200" or args.dataset == "nq_open_200":
            
            dataset_name = args.dataset.replace("_200", "")
    else:
            dataset_name = args.dataset.replace("simple_qa_", "")
    
    output_dir = 'outputs/LC_outputs'
    os.makedirs(output_dir, exist_ok=True)
    response_df.to_csv(os.path.join(output_dir, f"{safe_model_size.group(1)}_{dataset_name}_{style}.csv"), index=False)

